#Даний масив цілих чисел,
# повернути суму перших двох елементів у масиві.
# Якщо довжина масиву менше 2, просто скласти існуючі елементи,
# повертаючи 0, якщо довжина масиву дорівнює 0.

def sum2(nums):
    if len(nums) == 1:
        return nums[0]
    if len(nums) == 0:
        return 0
    return nums[0] + nums[1]

